package com.geog.model;

public class Region {
//	Instance variables	
	private String code;
	private String name;
	private String desc;
	
	
	
	
//	Constructor	
	public Region() {
	}
	
	
	
	
//	Accessors and mutators
	public String getCode() {
		return code;
	}
	
	public void setCode(String code) {
		this.code = code;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getDesc() {
		return desc;
	}
	
	public void setDesc(String desc) {
		this.desc = desc;
	}
	
} // class Region
